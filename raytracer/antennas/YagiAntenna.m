function antenna = YagiAntenna()

% Variable definitions ----------------------------------------------------
frequency = 2.45e9;
    
% director_length = [38.1 38.1 38.1 38.1 38.1  ...
%                   38.1 38.1 38.1 38.1 38.1  ...
%                   38.1 38.1 38.1 38.1 38.1  ...
%                   38.1 38.1 38.1];
% director_space = [12.2 28.6 41.3 41.3 41.3  ...
%                  44.5 44.5 44.5 44.5 44.5  ...
%                  47.6 44.5 47.6 47.6 47.6  ...
%                  47.6 41.3 44.5];
    
