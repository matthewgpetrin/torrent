antenna = yagiUda("Exciter", exciter, 'NumDirectors', 18, ...
                      "director_length", director_length.*10^-3, ...
                      "DirectorSpacing", director_space.*10^-3, ...
                      "reflector_length", reflector_length*10^-3, ...
                      "ReflectorSpacing", reflector_space*10^-3);
    
antenna.Tilt = 90;
antenna.TiltAxis = [0 1 0];